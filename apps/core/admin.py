from django.contrib import admin
from .models import AuditLog

@admin.register(AuditLog)
class AuditLogAdmin(admin.ModelAdmin):
    list_display = ('timestamp', 'user_mobile', 'action', 'resource_type', 'ip_address')
    list_filter = ('action', 'resource_type', 'user_role')
    search_fields = ('user_mobile', 'resource_id', 'correlation_id')
    readonly_fields = ('timestamp', 'user', 'user_mobile', 'user_role', 'action', 'resource_type', 
                      'resource_id', 'changes', 'reason', 'ip_address', 'correlation_id', 
                      'request_path', 'request_method')
    ordering = ('-timestamp',)

    def has_add_permission(self, request):
        return False

    def has_change_permission(self, request, obj=None):
        return False

    def has_delete_permission(self, request, obj=None):
        return False

